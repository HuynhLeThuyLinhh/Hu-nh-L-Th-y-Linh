a = int(input("Nhập số thứ nhất"))
b = int(input("Nhập số thứ hai"))
print("Tổng = ", a + b)
print("Hiệu = ", a - b)
print("Tích = ", a  * b)
print("Thương = ", a / b)
print("Thương = ", round (a/b,2), "(Làm tròn 2 chữ số) ")
print("Thương = ", round (a/b,3), "Làm tròn 3 chữ số) ")
print("Chia lấy dư = ", a % b)
print("Chia lấy nguyên = ", a // b)